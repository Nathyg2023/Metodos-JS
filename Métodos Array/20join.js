/*Método .join:

Este método une todos los elementos de una matriz en una cadena. Se puede especificar cualquier separador. El valor predeterminado es coma (,).*/

const elements = ['Fire', 'Air', 'Water'];

console.log(elements.join());
console.log(elements.join(''));
console.log(elements.join('-'));
console.log(elements.join('.'));









