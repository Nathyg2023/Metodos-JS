/*Método .includes():

Este método devuelve true si una matriz contiene un valor especificado y regresa false si no se encuentra el valor, distingue entre mayúsculas y minúsculas.*/

const array1 = [1, 2, 3];

console.log(array1.includes(2));


const pets = ['cat', 'dog', 'bat'];

console.log(pets.includes('cat'));
console.log(pets.includes('at'));




