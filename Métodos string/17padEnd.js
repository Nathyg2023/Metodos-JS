/*Método .padEnd():

Este método me ayuda a rellenar una cadena al final o en algunas oportunidades rellena una cadena con otra cadena (varias veces) hasta que alcanza una longitud determinada.*/


const str1 = 'Breaded Mushrooms';

console.log(str1.padEnd(25, '*'));

const str2 = '200';

console.log(str2.padEnd(5));

const str3 = "Nathy Gonzalez"

console.log(str3.padEnd(20, "-"))




