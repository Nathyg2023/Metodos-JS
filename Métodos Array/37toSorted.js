/*Método .toSorted():

Este método nos permite invertir el orden de un array. Devuelve un nuevo array con los elementos invertidos y no modifica el original.*/

const months = ["Mar", "Jan", "Feb", "Dec"];
const sortedMonths = months.toSorted();
console.log(sortedMonths); // ['Dec', 'Feb', 'Jan', 'Mar']
console.log(months); // ['Mar', 'Jan', 'Feb', 'Dec'] // [1, 2, 3]

















