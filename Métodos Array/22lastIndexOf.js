/*Método .lastIndexOf():

Este método devuelve devuelve el último índice (posición) de un valor especificado,  la búsqueda comienza en el último elemento y termina en el primero, los valores iniciales negativos cuentan desde el último elemento (pero siguen buscando de derecha a izquierda), método devuelve -1 si no se encuentra el valor*/

const animals = ['Dodo', 'Tiger', 'Penguin', 'Monkey'];

console.log(animals.lastIndexOf('Dodo'));
console.log(animals.lastIndexOf('Tiger'));
console.log(animals.lastIndexOf('Penguin'));
console.log(animals.lastIndexOf('Monkey'))












