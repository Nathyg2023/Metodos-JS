/*Método .unshift():

Este método agrega nuevos elementos al comienzo de una matriz, sobrescribe la matriz original.*/

const array1 = [1, 2, 3];
console.log(array1.unshift(4, 5));
console.log(array1);

const fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits.unshift("Lemon","Pineapple"));
console.log(fruits)

















