/*Método .indexOf():

Este método devuelve el primer índice (posición) de un valor especificaddo, devuelve -1 si no se encuentra el valor, comienza en un índice específico y busca de izquierda a derecha.De forma predeterminada, la búsqueda comienza en el primer elemento y finaliza en el último. Los valores iniciales negativos cuentan desde el último elemento (pero siguen buscando de izquierda a derecha).*/

