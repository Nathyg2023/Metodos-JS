/*Método .toReversed:

Este método es la contrapartida copiadora del reverse()método. Devuelve una nueva matriz con los elementos en orden inverso*/

const items = [1, 2, 3];
const reversedItems = items.toReversed();
console.log(reversedItems)
console.log(items);

console.log([1, 2, 3].toReversed()); // [3, undefined, 1]
console.log([1, 2, 3, 4].toReversed()); // [1, 2, 3]

















